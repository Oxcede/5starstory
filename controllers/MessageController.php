	 * Display a listing of the resource.
	 *
	 * @return Response
	 */
	public function index()
	{

        $messages = DB::table('messages')
            ->join('users', 'users.id', '=', 'messages.sender_user_id')
            ->select('users.id', 'users.username', 'messages.text')
            ->where('messages.receiver_user_id', '=', Auth::user()->id)
            ->paginate(2);

		return View::make('messages.messages', array('title'=>trans('voc.messages'), 'messages'=>$messages));

	}


